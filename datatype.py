integer_num = 38
float_num = 27.45

print("DATA TYPE DEMONSTRATION")
print("integer_num", integer_num,"type: ", type(integer_num))
print("float_num", float_num,"type: ",type(float_num))

int_to_float= float(integer_num)
print ("INTEGER TO FLOAT", int_to_float,"type", type(int_to_float))


float_to_int= int(float_num)
print("FLOAT TO INTEGER", float_to_int,"type", type(float_to_int))
